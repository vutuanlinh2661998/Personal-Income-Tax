    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        try {
            
            boolean status = true;
            int accountID= -1;
            String username = txtUsername.getText();
            String password = txtPassword.getText();
            int taxcode= Integer.parseInt(txtTaxcode.getText());
            status = checkTaxcode(taxcode);
            if (status=true){
                if (checkTaxcodeFromAccount(taxcode) == true) {
                    int stt = accDao.insertAccount(username, password, taxcode);
                    System.out.println(stt);
                    accountID = (int) accDao.selectAccount(username, password).get(0);
                    System.out.println(accountID);
                    String email = taxCodeDao.getTaxCodebyCode(taxcode).getEmail();
                    System.out.println(email);
                    String token = ActivationCode.getAlphaNumericString(10);
                    System.out.println(token);
                    SendEmailActivation.sendEmail(email, token);
                    int sttAc = activationCodeDao.insertActivationCode(token, new java.sql.Date (new java.util.Date().getTime()), accountID);
                    System.out.println(sttAc);
                    
                    new ActivationFrame(accountID).setVisible(true);
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Mã số thuế đã được đăng kí tài khoản!");
                }
                
            }else{
                JOptionPane.showMessageDialog(this, "Mã số thuế không tồn tại hoặc đã được đăng kí tài khoản!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            
        } catch (MessagingException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

