    public static boolean checkTaxcode (int taxcode) throws SQLException{
        boolean status = false;
        ArrayList<Integer> listTax = accDao.getAllTaxCode();
        if (listTax.contains(taxcode))
            status = true;
        return status;
    }
    
    public static boolean checkTaxcodeFromAccount (int taxcode) throws SQLException{
        boolean status = false;
        ArrayList<Integer> listTax = accDao.getAllTaxCodeFromAccount();
        if (!listTax.contains(taxcode))
            status = true;
        return status;
    }
    
