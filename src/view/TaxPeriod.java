    private void taxPeriodComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_taxPeriodComboActionPerformed
    
            if(taxPeriodCombo.getSelectedIndex()==0){
                startMonth.setText("1/"+txtYear.getText());
                endMonth.setText("3/"+txtYear.getText());
                
            }else  if(taxPeriodCombo.getSelectedIndex()==1){
                startMonth.setText("4/"+txtYear.getText());
                endMonth.setText("6/"+txtYear.getText());
                
            } else  if(taxPeriodCombo.getSelectedIndex()==2){
                startMonth.setText("7/"+txtYear.getText());
                endMonth.setText("9/"+txtYear.getText());
                
            } else  if(taxPeriodCombo.getSelectedIndex()==3){
                startMonth.setText("10/"+txtYear.getText());
                endMonth.setText("12/"+txtYear.getText());
            }else {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn kì nộp thuế!");
            }
        
    }//GEN-LAST:event_taxPeriodComboActionPerformed

