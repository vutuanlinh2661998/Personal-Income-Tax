    private void btnAccessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccessActionPerformed
       
        if(!startMonth.getText().equals("") && !endMonth.getText().equals("")){
            int month = Calendar.getInstance().get(Calendar.MONTH)+1;
            System.out.println(month);
            if (taxPeriodCombo.getSelectedIndex()==0 &&  month <=3){
                 JOptionPane.showMessageDialog(this, "Hiện tại chưa hết thời gian của Quý 1, vui lòng chọn lại kì khai báo!");
            }else if (taxPeriodCombo.getSelectedIndex()==1 && month <=6){
                 JOptionPane.showMessageDialog(this, "Hiện tại chưa hết thời gian của Quý 2, vui lòng chọn lại kì khai báo!");
            }else if (taxPeriodCombo.getSelectedIndex()==2 && month <=9){
                 JOptionPane.showMessageDialog(this, "Hiện tại chưa hết thời gian của Quý 3, vui lòng chọn lại kì khai báo!");
            } else if (taxPeriodCombo.getSelectedIndex()==3 && month <=12){
                 JOptionPane.showMessageDialog(this, "Hiện tại chưa hết thời gian của Quý 4, vui lòng chọn lại kì khai báo!");
            }else{
            
        String taxPeriod = "Quý"+taxPeriodCombo.getSelectedItem().toString()+" ( Từ tháng "+startMonth.getText()+" đến tháng "+endMonth.getText()+")";
        String start = null;
        String time = null;
        if (jRadioButton1.isSelected()){
            start = "[X]";
            time = null;
        }else{
            start = null;
            time= txtTime.getText();
        }
        new TaxDeclareView(userAccountId ,taxPeriod, start, time,name,taxcode).setVisible(true);
        this.dispose();
            
            
            }
        }else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn quý khai báo thuế!");
        }
    }//GEN-LAST:event_btnAccessActionPerformed

